    private void btnAlterarAviActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarAviActionPerformed
        try {
            //Chama o m�todo abrirBD da classe AviaoDAL            
            dal.abrirBD();
            //Fazendo a valida��o dos dados, o m�todo isEmpty que devolve true se a String for vazia ou false se a mesma n�o estiver vazia
            if (txtCodigoAvi.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "O campo código do Avião está vazio !!!!");
                txtCodigoAvi.requestFocus();
            } else if (txtFabricanteAvi.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "O campo Fabricante do avião está vazio !!!!");
                txtFabricanteAvi.requestFocus();
            } else if (txtModeloAvi.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "O campo Modelo do Avião está vazio !!!!");
                txtModeloAvi.requestFocus();
            } else {
                aviao.setAviID(Long.parseLong(txtCodigoAvi.getText()));
                aviao.setAviFabricante(txtFabricanteAvi.getText());
                aviao.setAviModelo(txtModeloAvi.getText());
                try {
                    //Chama o m�todo alterarAviao da Classe AviaoDAL
                    dal.alterarAviao(aviao);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Erro Na Conexão. Verifique com o Administrador do Banco de Dados !!!!");
                }
                JOptionPane.showMessageDialog(null, "Avião " + aviao.getAviModelo() + " Alterado Com Sucesso !!!!");
                //Limpa os dados preenchidos nos campos de texto
                txtCodigoAvi.setText(null);
                txtFabricanteAvi.setText("");
                txtModeloAvi.setText("");
                txtCodigoAvi.requestFocus();
                btnMostrarTodosAvi.setEnabled(true);
                //Chama o m�todo fecharBD da classe AviaoDAL
                dal.fecharBD();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "O campo Modelo do Avião está  vazio !!!!");
            txtModeloAvi.requestFocus();
        }
    }//GEN-LAST:event_btnAlterarAviActionPerformed

