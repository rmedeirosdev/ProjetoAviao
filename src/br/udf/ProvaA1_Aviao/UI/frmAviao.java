    private void btnIncluirAviActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirAviActionPerformed
        try {
            //Chama o método abrirBD da classe AviãoDAL
            dal.abrirBD();
            aviao.setAviFabricante(txtFabricanteAvi.getText());
            aviao.setAviModelo(txtModeloAvi.getText());

            // fazendo a validação dos dados
            if (txtFabricanteAvi.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "O campo Fabricante do Avião está vazio !!!!");
                txtFabricanteAvi.requestFocus();
            } else if (txtModeloAvi.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "O campo Modelo do Avião está vazio !!!!");
                txtModeloAvi.requestFocus();
            } else {
                try {
                    //Chama o método incluirAviao da classe AviaoDAL
                    dal.incluirAviao(aviao);
                    JOptionPane.showMessageDialog(null, "Avião " + txtModeloAvi.getText() + " incluído com sucesso! ");
                    //Chama o método fecharBD da classe AviãoDAL
                    dal.fecharBD();
                    //Limpa os dados preenchidos nos campos de texto
                    txtFabricanteAvi.setText("");
                    txtModeloAvi.setText("");
                    txtFabricanteAvi.requestFocus();
                    btnMostrarTodosAvi.setEnabled(true);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Erro Na Conexão. Verifique com o Administrador do Banco de Dados !!!!");
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "O campo Modelo do avião está vazio !!!!");
            txtModeloAvi.requestFocus();
        }
    }//GEN-LAST:event_btnIncluirAviActionPerformed

    private void btnPesquisarAviIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarAviIDActionPerformed

        //Verifica se o código do aviao está vazio
        if ("".equals(txtCodigoAvi.getText())) {
            JOptionPane.showMessageDialog(null, "Código do Avião não pode ser vazio, favor digite um código válido !!!!");
            //Após a mensagem de erro, o foco do cursor vai ficar no campo Código do Aviao 
            txtCodigoAvi.requestFocus();
        } else {
            //Faz o tratamento de erro, para verificar se a tabela Avião está vazia
            //O botão Incluir vai ficar dsesabilitado
            btnIncluirAvi.setEnabled(false);
            //O botão Alterar vai ficar habilitado
            btnAlterarAvi.setEnabled(true);
            //O botão Excluir vai ficar habilitado
            btnExcluirAvi.setEnabled(true);
            //O botão Mostrar Todos vai ficar habilitado
            btnMostrarTodosAvi.setEnabled(true);
            //Mostra os dados do aviao pelo seu código
            txtFabricanteAvi.setText(aviao.getAviFabricante());
            txtModeloAvi.setText(aviao.getAviModelo());
            try {
                //Chama o método selecionarAviaoPorID da classe AviaoDAL
                aviao = dal.selecionarAviaoPorID(Integer.valueOf(txtCodigoAvi.getText()));
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Avião Não Encontrado. Favor Tente Novamente !!!!");
                txtCodigoAvi.setText(null);
                txtCodigoAvi.requestFocus();
            }
        }
    }//GEN-LAST:event_btnPesquisarAviIDActionPerformed

    private void btnExcluirAviActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirAviActionPerformed
        aviao.setAviID(Long.parseLong(txtCodigoAvi.getText()));
        Object[] opcoes = {"Sim", "Não"};
        int contador = JOptionPane.showOptionDialog(null, "Deseja Excluir Este Avião: " + txtModeloAvi.getText() + "?",
                "Exclusão", JOptionPane.YES_OPTION, JOptionPane.QUESTION_MESSAGE, null, opcoes, opcoes[0]);
        if (contador == JOptionPane.YES_OPTION) {
            try {
                //Chama o método abrirBD da classe AviaoDAL            
                dal.abrirBD();
                //Chama o método excluirAviao da classe AviaoDAL 
                dal.excluirAviao(Integer.valueOf(txtCodigoAvi.getText()));
                JOptionPane.showMessageDialog(null, "Avião Excluído com Sucesso !!!!");
                //apaga os dados preenchidos nos campos de texto abaixo
                txtCodigoAvi.setText(null);
                txtFabricanteAvi.setText("");
                txtModeloAvi.setText("");
                txtCodigoAvi.requestFocus();
                //Chama o método fecharBD da classe AviaoDAL
                dal.fecharBD();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Erro Na Conexão. Verifique com o Administrador do Banco de Dados !!!!");
            }
        }
    }//GEN-LAST:event_btnExcluirAviActionPerformed

    private void btnMostrarTodosAviActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarTodosAviActionPerformed
        //Limpa todas as caixas de texto
        txtFabricanteAvi.setText("");
        txtModeloAvi.setText("");

        //Cria a lista de todos os avioes dentro da tabela aviao
        List<AviaoDTO> selecionaTodosAviao = new ArrayList<>();
        try {
            //Chama o método abrirBD da classe AviaoDAL            
            dal.abrirBD();
            //Chama o m�todo selecionaTodosAviao da Classe AviaoDAL e preeche o mesmo com a lista de todos os avioes.
            selecionaTodosAviao = dal.selecionarListaAviao();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro Na Conexão. Verifique com o Administrador do Banco de Dados !!!!");
        }
        //Prepara a tabela para receber os dados da busca(Lista)
        DefaultTableModel tabm = (DefaultTableModel) dgvAviao.getModel();
        for (int contador = tabm.getRowCount() - 1; contador >= 0; contador--) {
            tabm.removeRow(contador);
        }
        int contador = 0;
        for (AviaoDTO aviao : selecionaTodosAviao) {
            //Mostra as informa��es dentro da Tabela
            tabm.addRow(new String[1]);
            dgvAviao.setValueAt(aviao.getAviID(), contador, 0);
            dgvAviao.setValueAt(aviao.getAviFabricante(), contador, 1);
            dgvAviao.setValueAt(aviao.getAviModelo(), contador, 2);
            contador++;
        }

        try {
            //Limpa o campo Código do Aviao
            txtCodigoAvi.setText(null);
            //Posiciona o cursor do mouse no campo Modelo do aviao
            txtModeloAvi.requestFocus();
            //Chama o método fecharBD da classe AviaoDAL
            dal.fecharBD();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro Na Conexão. Verifique com o Administrador do Banco de Dados !!!!");
        }
    }//GEN-LAST:event_btnMostrarTodosAviActionPerformed

